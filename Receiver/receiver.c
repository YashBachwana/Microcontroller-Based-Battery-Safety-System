float map_adc_to_temperature(uint16_t adc_val) {
	float voltage = (0.3918 * adc_val) - 153.07;
	if (adc_val < 480){
		voltage = 31.34;
	}
	return (voltage);
}

float map_adc_to_smoke(uint16_t adc_val) {
	float voltage = (0.730 * adc_val) - 461.54;
	if (adc_val < 700){
		voltage = 50;
	}
	return (voltage);
}

float map_voltage(float T, float Smoke){
	float voltage = 3.7 - 1.2 * (0.5 * ((T - 31.3) / (68.7)) + 0.5*((Smoke - 50) / (950)));
	return (voltage);
}

float map_adc_to_pressure(uint16_t adc_val) {
	float voltage = (0.000589 * adc_val) + 0.587;
	if (adc_val < 700){
		voltage = 0.9 ;
	}
	return (voltage);
}


void print_decimal_1(float number)
{
	int integerPart = (int)number;
	int decimalPart = (int)round((number - integerPart) * 10);
	if (decimalPart == 10) {
	integerPart += 1;
	decimalPart = 0;
	}
	lcd16x2_i2c_printf("%01d.%01d", integerPart, decimalPart);
}

void print_decimal_2(float number)
{
	int integerPart = (int)number;
	int decimalPart = (int)round((number - integerPart) * 100);
	if (decimalPart == 100) {
	integerPart += 1;
	decimalPart = 0;
	}
	lcd16x2_i2c_printf("%01d.%02d", integerPart, decimalPart);
}

void display(void)
{
	lcd16x2_i2c_setCursor(0,0);
	print_decimal_1(pressure);
	lcd16x2_i2c_setCursor(0,4);
	lcd16x2_i2c_printf("atm");

	lcd16x2_i2c_setCursor(1,0);
	print_decimal_1(temperature);
	lcd16x2_i2c_setCursor(1,5);
	lcd16x2_i2c_printf("C");

	lcd16x2_i2c_setCursor(0,8);
	print_decimal_1(smoke);
	lcd16x2_i2c_setCursor(0,13);
	lcd16x2_i2c_printf("ppm");

	lcd16x2_i2c_setCursor(1,8);
	print_decimal_2(voltage);
	lcd16x2_i2c_setCursor(1,13);
	lcd16x2_i2c_printf("V");

	if (evacuate == 1){
		__HAL_TIM_SET_PRESCALER(&htim1, 99);
		HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_1);

	}
	if (evacuate == 0 & cool_down == 1){
		__HAL_TIM_SET_PRESCALER(&htim1, 9999);
		HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_1);
	}
	if (evacuate == 0 & cool_down == 0){
		HAL_TIM_PWM_Stop(&htim1,TIM_CHANNEL_1);
	}
//	lcd16x2_i2c_printf("Hello");
}
